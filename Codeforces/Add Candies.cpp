#include <bits/stdc++.h>
using namespace std;
#define ll long long
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    ll t;
    cin>>t;
    while(t--)
    {
        ll n;
        cin>>n;
        cout<<n<<endl;
        for(ll i=1;i<=n;i++)
        {
            cout<<i<<" ";
        }
        cout<<endl;
    }
    return 0;
}

